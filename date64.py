import numpy as np
year=np.datetime64('2025','Y')
month=np.datetime64('2025-04','M')
day=np.datetime64('2025-04-19','D')
hour=np.datetime64('2025-04-29T10','h')
second=np.datetime64('2025-04-29T10:30:15','s')


print("Year:",year)
print("Month:",month)
print("Day:",day)
print("Hour:",hour)
print("Second:",second)
